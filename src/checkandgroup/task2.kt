package checkandgroup

fun main() {
    val list = listOf(
        "Арбуз", "Банан", "Дыня", "Виноград", "Ель",
        "Ежевика", "Голубика", "Помидор", "Клубника",
        "Огурец", "Картофель", "Яблоко", "Груша",
        "Жимолость", "Тыква", "Малина", "Киви",
        "Ананас"
    )

    val result = list
        .groupBy { it.length }  //группируем по длине строки

    println(result)
}